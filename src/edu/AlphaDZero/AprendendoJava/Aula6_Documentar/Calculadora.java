/**
 * <h1>Calculadora</h1>
 * Essa classe é uma calculadora simples
 * <p>
 * <b>Note:</b> Leia a documentação dessa classe, para que não haja duvidas
 * 
 * @author AlphaDZero
 * @version 1.0
 * @since 07/03/2023
 */
public class Calculadora {
    /**
     * Esse método realiza uma soma de dois numeros.
     * @param numero1 primeiro parâmetro, um número inteiro ou racional. 
     * @param numero2 segundo parâmetro, um número inteiro ou racional.  
     * @return retorna um somatório dos parâmetros.
     */
    public double somar(double numero1, double numero2){
        return numero1 + numero2;
    }
}
